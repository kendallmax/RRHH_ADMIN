returns void
language plpgsql
security definer
set search_path = public, auth
as $$
begin
  perform public.assert_hr_admin();

  delete from public.attendance_locations
  where id = location_id;

  if not found then
    raise exception 'No encontramos el lugar solicitado.';
  end if;
end;
$$;

grant execute on function public.admin_delete_attendance_location(bigint) to authenticated;
