create or replace function public.admin_upsert_attendance_location(
  location_id bigint default null,
  location_name text default null,
  location_requires_description boolean default false,
  location_sort_order integer default 0,
  location_is_active boolean default true
)
returns public.attendance_locations
language plpgsql
security definer
set search_path = public, auth
as $$
declare
  saved_row public.attendance_locations;
begin
  perform public.assert_hr_admin();

  if location_name is null or btrim(location_name) = '' then
    raise exception 'El nombre del lugar es obligatorio.';
  end if;

  if location_id is null then
    insert into public.attendance_locations (
      name,
      requires_description,
      sort_order,
      is_active
    )
    values (
      btrim(location_name),
      location_requires_description,
      coalesce(location_sort_order, 0),
      location_is_active
    )
    returning * into saved_row;
  else
    update public.attendance_locations
    set
      name = btrim(location_name),
      requires_description = location_requires_description,
      sort_order = coalesce(location_sort_order, 0),
      is_active = location_is_active
    where id = location_id
    returning * into saved_row;

    if saved_row is null then
      raise exception 'No encontramos el lugar solicitado.';
    end if;
  end if;

  return saved_row;
end;
$$;

grant execute on function public.admin_upsert_attendance_location(bigint, text, boolean, integer, boolean) to authenticated;

