create or replace function public.is_current_user_hr_admin()
returns boolean
language sql
security definer
stable
set search_path = public, auth
as $$
  select coalesce(
    (
      select
        coalesce((u.raw_user_meta_data ->> 'is_admin')::boolean, false)
        or coalesce(u.raw_user_meta_data ->> 'role' = 'admin', false)
        or coalesce(u.raw_app_meta_data ->> 'role' = 'admin', false)
      from auth.users u
      where u.id = auth.uid()
    ),
    false
  );
$$;

create or replace function public.assert_hr_admin()
returns void
language plpgsql
security definer
set search_path = public, auth
as $$
begin
  if not public.is_current_user_hr_admin() then
    raise exception 'Acceso denegado. Solo administradores de RRHH pueden ejecutar esta accion.';
  end if;
end;
$$;

create or replace function public.get_employee_directory()
returns table (
  user_id uuid,
  email text,
  display_name text,
  nombre text,
  apellidos text,
  is_admin boolean,
  is_active boolean,
  created_at timestamptz
)
language plpgsql
security definer
set search_path = public, auth
as $$
begin
  perform public.assert_hr_admin();

  return query
  select
    u.id as user_id,
    u.email::text as email,
    coalesce(
      nullif(u.raw_user_meta_data ->> 'display_name', ''),
      nullif(u.raw_user_meta_data ->> 'full_name', ''),
      nullif(trim(concat_ws(' ', u.raw_user_meta_data ->> 'nombre', u.raw_user_meta_data ->> 'apellidos')), ''),
      split_part(u.email::text, '@', 1)
    ) as display_name,
    coalesce(u.raw_user_meta_data ->> 'nombre', '') as nombre,
    coalesce(u.raw_user_meta_data ->> 'apellidos', '') as apellidos,
    coalesce((u.raw_user_meta_data ->> 'is_admin')::boolean, false)
      or coalesce(u.raw_user_meta_data ->> 'role' = 'admin', false)
      or coalesce(u.raw_app_meta_data ->> 'role' = 'admin', false) as is_admin,
    coalesce((u.raw_user_meta_data ->> 'is_active')::boolean, true) as is_active,
    u.created_at
  from auth.users u
  order by 3 asc, 2 asc;
end;
$$;

grant execute on function public.get_employee_directory() to authenticated;

create or replace function public.get_attendance_report(
  filter_user_id uuid default null,
  filter_start_date date default null,
  filter_end_date date default null
)
returns table (
  id text,
  user_id uuid,
  employee_name text,
  employee_email text,
  attendance_date text,
  attendance_time text,
  tipo text,
  ubicacion text,
  descripcion text,
  latitud double precision,
  longitud double precision,
  ip text,
  created_at timestamptz
)
language plpgsql
security definer
set search_path = public, auth
as $$
begin
  perform public.assert_hr_admin();

  return query
  select
    a.id::text,
    a.user_id,
    coalesce(
      nullif(u.raw_user_meta_data ->> 'display_name', ''),
      nullif(u.raw_user_meta_data ->> 'full_name', ''),
      nullif(trim(concat_ws(' ', u.raw_user_meta_data ->> 'nombre', u.raw_user_meta_data ->> 'apellidos')), ''),
      split_part(u.email::text, '@', 1)
    ) as employee_name,
    u.email::text as employee_email,
    to_char(timezone('America/Costa_Rica', a.created_at), 'YYYY-MM-DD') as attendance_date,
    to_char(timezone('America/Costa_Rica', a.created_at), 'HH24:MI:SS') as attendance_time,
    a.tipo::text,
    a.ubicacion::text,
    a.descripcion::text,
    a.latitud,
    a.longitud,
    a.ip::text,
    a.created_at
  from public.asistencias a
  join auth.users u on u.id = a.user_id
  where (filter_user_id is null or a.user_id = filter_user_id)
    and (
      filter_start_date is null
      or timezone('America/Costa_Rica', a.created_at)::date >= filter_start_date
    )
    and (
      filter_end_date is null
      or timezone('America/Costa_Rica', a.created_at)::date <= filter_end_date
    )
  order by employee_name asc, attendance_date asc, a.created_at asc;
end;
$$;

grant execute on function public.get_attendance_report(uuid, date, date) to authenticated;

